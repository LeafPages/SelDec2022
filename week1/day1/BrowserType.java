package week1.day1;

public class BrowserType {
	
	
	public static void main(String[] args) {
		
		String browser = "safari";
		
		// switch - case : int / String (1.7)
		
		switch(browser) {
		
		case "chrome": System.out.println("Google"); break;
		case "edge": System.out.println("Microsoft"); break;
		case "safari": System.out.println("Apple"); break;
		case "firefox": System.out.println("Mozillo"); break;
		
		
		}
		
		
	}

}
